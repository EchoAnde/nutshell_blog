function getUnreadMessageCount() {
    var $unreadcountLabel = $('[nut-unreadcount-label]');
    if ($unreadcountLabel) {
        $.post('/message/GetUnreadMessageCount', {}, function (data) {
            var count = 0;
            if (!data.code && data.code != 1) {
                count = data;
            } else {
                count = 0;
            }

            $unreadcountLabel.text(count);
            $unreadcountLabel.parents('a').attr('title', '您有' + count + '条未读消息');
        });
    }
}